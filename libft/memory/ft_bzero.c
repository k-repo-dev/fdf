#include "../libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	char			*tptr;

	tptr = (char *) s;
	i = 0;
	while (i < n)
	{
		tptr[i] = 0;
		i++;
	}
}
